const applyTranslations = (lang) => {
    const dict = translations[lang] || translations.ru;
    document.documentElement.lang = lang;

    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        const value = dict[key];
        if (!value) return;
        const attr = el.dataset.i18nAttr;
        if (attr) {
            el.setAttribute(attr, value);
        } else {
            el.textContent = value;
        }
    });

    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const key = el.dataset.i18nHtml;
        const value = dict[key];
        if (!value) return;
        el.innerHTML = value;
    });
};

const setupLanguageSwitcher = () => {
    const buttons = document.querySelectorAll('.lang-btn');
    if (!buttons.length) return;

    const saved = localStorage.getItem('smartcare-lang');
    const initial = saved || document.documentElement.lang || 'ru';
    applyTranslations(initial);
    buttons.forEach(btn => btn.classList.toggle('active', btn.dataset.lang === initial));

    buttons.forEach(btn => {
        btn.addEventListener('click', () => {
            const lang = btn.dataset.lang;
            applyTranslations(lang);
            localStorage.setItem('smartcare-lang', lang);
            buttons.forEach(b => b.classList.toggle('active', b.dataset.lang === lang));
        });
    });
};

setupLanguageSwitcher();

